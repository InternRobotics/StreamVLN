    @property
    def modality_lengths(self):
        # Estimate the number of tokens after tokenization, used for length-grouped sampling
        length_list = []
        for info in self.data_list:
            num_images = min(6, len(info["image_info"]))
            sentences = [info["text_list"][x["matched_text_index"]] for x in info["image_info"][:num_images]]
            # The unit of cur_len is "words". We assume 1 word = 2 tokens.
            cur_len = num_images * self.num_image_tokens // 2 + sum([len(x) for x in sentences])
            length_list.append(cur_len)
        return length_list
    
    def prepare_conservation(self, sentences, sentence_ixs): 
        sources = []
        # [{'from':'human', 'value': sources['text']},{'from': 'gpt','value': ""}]
        for i in range(len(sentence_ixs)):
            source =copy.deepcopy([{'from':'human', 'value': ''},{'from':'gpt','value': ''}])
            if i ==0:
                prompt = "".join(sentences[:sentence_ixs[i]]) + DEFAULT_IMAGE_TOKEN
            else:
                prompt = DEFAULT_IMAGE_TOKEN
            source[0]['value'] = f"{prompt}."
                
            # up_to = min(i+1, len(sentence_ixs) - 1)
            if i == len(sentence_ixs) - 1:
                anwser = "".join(sentences[sentence_ixs[i]:])
            else:
                anwser = "".join(sentences[sentence_ixs[i]:sentence_ixs[i+1]])
            source[1]['value'] = anwser
            sources.extend(source)
        
        return sources


